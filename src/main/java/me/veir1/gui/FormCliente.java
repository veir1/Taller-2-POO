    private void buttonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEnviarActionPerformed
        String data = "";
        if (fieldRut.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el RUT del alumno a procesar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }                                
        if (fieldNombre.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese los nombres del alumno a procesar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (fieldCorreo.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el email del alumno a procesar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (blockButtonGroup.getSelection() == null) {
            JOptionPane.showMessageDialog(null, "Por favor seleccione el bloque del alumno a registrar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        char block;
        if (optBloque1.isSelected()) {
            block = 'A';
        } else {
            block = 'B';
        }

        final Entity entity = new Entity(fieldRut.getText(), fieldNombre.getText(), fieldCorreo.getText(), block);
        peopleStorage
                .addEntity(
                        entity);

        fieldRut.setText("");
        fieldNombre.setText("");
        fieldCorreo.setText("");

        blockButtonGroup.clearSelection();

    }//GEN-LAST:event_buttonEnviarActionPerformed

